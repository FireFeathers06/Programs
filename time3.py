#Time string formats
#strftime()
import time
print('day in 3 letters %a --',time.strftime('%a'))
print('day %A --',time.strftime('%A'))
print('month in 3 letters %b --',time.strftime('%b'))
print('month in %B --',time.strftime('%B'))
print('date and time %c --',time.strftime('%c'))
print('%C',time.strftime('%C'))
print('date %d --',time.strftime('%d'))
print('date %D --',time.strftime('%D'))
print('date %F --',time.strftime('%F'))
